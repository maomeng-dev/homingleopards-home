                <p>华北豹（<i class="text-italic">Panthera pardus japonensis</i>），这个中国特有豹亚种的模式标本采自19世纪的北京。对于有北京户口的华北豹来说，北京是它的家。
                </p>
                <p>而从生境的角度，太行山脉和燕山山脉是一个连续完整的华北豹栖息地，豹会沿着山脉扩散、建立领地，两山交汇的北京拥有超过10000平方公里的山地面积，是它们的重要家园。</p>
                <p>过去，河北、山西的华北豹可以沿着太行山脉一路进京，也可以一路南下进入河南乃至陕西秦岭。</p>
                <p>但是，近50年来，这条天然的扩散之路不再畅通。公路切断山脉、村庄和农田蚕食山林，栖息地面积缩减，兽夹、电网、钢丝套屡禁不绝，野猪、狍子等猎物也随之减少；华北豹的扩散空间越来越有限，过去完整的种群逐渐孤岛化，残存的个体只能走向消亡。</p>
                <p>2005年后，北京就再也没发现过华北豹的身影。</p>
                <p>带豹回家，就是让消失的华北豹重新回到北京，这便意味着修复扩散廊道，唤醒人们对身边荒野的关注和守护的热情，让人与自然学会和谐地共处，让太行山脉重新焕发生机，长出灵魂。</p>
                <p>一言以蔽之，以豹之名，修复华北的荒野与人心。</p>
